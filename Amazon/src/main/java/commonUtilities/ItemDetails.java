package commonUtilities;

public class ItemDetails {
	protected String Name;
	protected Float Prize;

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

	public Float getPrize() {
		return Prize;
	}

	public void setPrize(Float prize) {
		Prize = prize;
	}

}
